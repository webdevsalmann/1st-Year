// =============================================Create card=========================

let contentBlock = document.getElementById("content-block")
for (let i of products.data) {


    let iCat = i.category;
    let iSubCat = i.subCategory;
    let iName = i.shownName;
    let iDesc = i.showDescription;
    let iLink = i.link;
    let iImg = i.image;


    //Create Card
    let card = document.createElement("div");
    //Card should have category and should stay hidden initially
    card.classList.add("card", iCat, "hide");
    card.setAttribute("data-category", iCat);


    //Create imageBox
    let imageBox = document.createElement("div");
    imageBox.classList.add("image-box");
    //Create images
    let image = document.createElement("img");
    image.classList.add("image");
    image.setAttribute("src", iImg);
    //Create detail
    let detail = document.createElement("p");
    detail.classList.add("detail");


    //Create info-box
    let infoBox = document.createElement("div");
    infoBox.classList.add("info-box");
    //Create webName
    let webName = document.createElement("div");
    webName.classList.add("web-name");
    //Create btn
    let btn = document.createElement("a");
    btn.classList.add("btn");
    btn.setAttribute("href", i.link);
    btn.setAttribute("target", "_blank");


    //   append in card
    card.appendChild(imageBox);
    card.appendChild(infoBox);
    //   append in imageBox
    imageBox.appendChild(image);
    imageBox.appendChild(detail);
    //   append in infoBox
    infoBox.appendChild(webName);
    infoBox.appendChild(btn);


    //   inner Text 
    webName.innerText = iName;
    detail.innerText = i.showDescription;
    btn.innerText = "Open";


    //   append in ContentBox show in display
    contentBlock.appendChild(card);
}

