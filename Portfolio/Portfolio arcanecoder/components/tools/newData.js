// create an empty array to store the unique category values
let uniqueCategories = [];
// loop through the data array
for (let i = 0; i < products.data.length; i++) {
    let product = products.data[i];

    // check if the current product's category value has already been added to the uniqueCategories array
    if (!uniqueCategories.includes(product.category)) {
        // Add if not, add it to the uniqueCategories array
        uniqueCategories.push(product.category);

        // left side bar button list 
        let navBtnList = document.getElementById("nav_btn-list");
        //Create 
        let btnShow = document.createElement("li");
        btnShow.classList.add("btn_show");
        btnShow.innerText = product.category;
        // append to navBtnList 
        navBtnList.appendChild(btnShow);
    }
}

