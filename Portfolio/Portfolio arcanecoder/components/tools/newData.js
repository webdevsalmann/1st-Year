// Show card on sidebar button click 
let btnComponents = Array.from(document.querySelectorAll(".btn_show"));
let cards = document.querySelectorAll(".card");

let currentLi = null;
btnComponents.forEach(btn => {
    btn.addEventListener('click', (e) => {

        // change backgroundColor of selected li 
        if (currentLi !== null) {
            currentLi.style.backgroundColor = '';
            currentLi.style.color = '';
        }
        currentLi = e.target;
        currentLi.style.backgroundColor = '#FCFFE7';
        currentLi.style.color = '#EB455F';


        // Hide all cards
        cards.forEach(card => {
            card.classList.add("hide");
        });

        // Get references to the sidebar button and card elements
        let categoryBtnValue = e.explicitOriginalTarget.innerText.toLowerCase().replace(/\s+/g, '');
        let matchingCards = document.querySelectorAll(`[data-category="${categoryBtnValue}"]`);
        // console.log(matchingCards);

        // Show cards matching the selected category
        matchingCards.forEach(card => {
            if (!matchingCards) {
                console.log("not found category")
            } else if (matchingCards) {
                card.classList.remove("hide");
            } else {
                card.classList.add("hide");
            }
        });

    });
});






