const testimonialLis = document.querySelectorAll("section.review .profile-img-container li");
const testimonialContainers = document.querySelectorAll("section.review .testimonial-container");


testimonialLis.forEach(li => {
    li.addEventListener("click", () => {
        let target = document.querySelector(li.dataset.tabTarget)
        testimonialContainers.forEach(testimonialContainer => {
            testimonialContainer.classList.remove('active')
        })
        testimonialLis.forEach(li => {
            li.classList.remove("active");
        });
        li.classList.add("active");
        target.classList.add("active");
    })
})




