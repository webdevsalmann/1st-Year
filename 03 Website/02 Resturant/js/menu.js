let displayContainer;
function createCard(item) {
    let jName = item.name;
    let jDesc = item.description;
    let jImg = item.image;
    let jPricing = item.price;

    //Create Card
    let card = document.createElement("div");
    card.classList.add("card");
    //Create imageBox
    let imageBox = document.createElement("div");
    imageBox.classList.add("img-box");
    //Create img element
    let image = document.createElement("img");
    image.setAttribute("src", jImg);
    imageBox.appendChild(image);

    //Create detail-box
    let detailBox = document.createElement("div");
    detailBox.classList.add("detail-box");
    //Create text-box
    let textBox = document.createElement("div");
    textBox.classList.add("text-box");
    //Create webName
    let name = document.createElement("span");
    name.classList.add("name", "h5");
    name.innerText = jName;
    //Create Price
    let price = document.createElement("span");
    price.classList.add("price");
    price.innerText = "â‚¹" + jPricing;
    textBox.appendChild(name);
    textBox.appendChild(price);

    //Create btn-box
    let btnBox = document.createElement("div");
    btnBox.classList.add("btn-box");
    //Create btn-1
    let btnOrder = document.createElement("button");
    btnOrder.classList.add("btn", "btn-p");
    let btnPSpan = document.createElement("span");
    btnPSpan.innerText = "Order Now";
    btnOrder.appendChild(btnPSpan);
    //Create btn-2
    let btnAdd = document.createElement("button");
    btnAdd.classList.add("btn", "btn-s");
    let btnSSpan = document.createElement("span");
    btnSSpan.innerText = "Add to Cart";
    btnAdd.appendChild(btnSSpan);
    btnBox.appendChild(btnAdd);
    btnBox.appendChild(btnOrder);

    //   appending inside card
    card.appendChild(imageBox);
    card.appendChild(detailBox);
    detailBox.appendChild(textBox);
    detailBox.appendChild(btnBox);

    //   append in displayCardSection which is inside sub-category-section
    displayContainer.appendChild(card);
}

for (let i of menu.starter) {
    for (let j of i.list) {
        displayContainer = document.querySelector("#starter" + " .card-container");
        createCard(j);
    }
}

